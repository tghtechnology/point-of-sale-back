class Cliente{
    constructor(id, nombre, email, telefono, direccion, ciudad, region, pais, fecha_creacion, fecha_modificacion, estado){
        this.id = id
        this.nombre = nombre
        this.email = email
        this.telefono = telefono
        this.direccion = direccion
        this.ciudad = ciudad
        this.region = region
        this.pais = pais
        this.fecha_creacion = fecha_creacion
        this.fecha_modificacion = fecha_modificacion
        this.estado = estado
    }
}
export default Cliente