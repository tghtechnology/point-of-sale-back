class Cliente{
    constructor(id, nombre, email, telefono, direccion, ciudad, region, codigo_postal, pais, estado){
        this.id = id
        this.nombre = nombre
        this.email = email
        this.telefono = telefono
        this.direccion = direccion
        this.ciudad = ciudad
        this.region = region
        this.codigo_postal = codigo_postal
        this.pais = pais
        this.estado = estado
    }
}
export default Cliente